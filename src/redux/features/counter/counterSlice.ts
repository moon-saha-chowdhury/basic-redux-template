import { createSlice } from "@reduxjs/toolkit";

const initialState = {
  count: 0,
};

const counterSlice = createSlice({
  name: "counter",
  initialState,
  reducers: {
    increment: (state) => {
      state.count = state.count + 1;
    },
    decrement: (state) => {
      state.count = state.count - 1;
    },
  }, //reducer a amra j action gulo perform korbo tar nam thakbe
  //ai reducer a duita jinish thake state and jodi input hisebe kono payload pay
});
